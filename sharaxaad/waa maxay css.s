# waa maxay css3 

css waxa loo soo gaabiyay (Cascading Style Sheet )
wa luuqadda website keena qurxisa waxa aynu hore usoo sheegnay 
in html yahay qolfoof ka ama seeska website ama webpage 
hadaba css iyadu ay macno u samaysa html kii oo waxay ka dhigta 
mid ah ujeedada loo socday oo ah website ugu yaraan webpage kii la doonayay 
css waxa ay hada maraysa version 3 
waana version kii ugu dambeyey ee 2015 lasoo saaro 

css waxay leedahay awoodo faro badan taaso dhaxalsiisay in ay hogaanka u qabato dhanka design ka iyo quruxda 
malaayiinta website ee ku jira internetka gudihiisa 
hadaba 
css mar kaligeed aya website yo faro badan lagu dhisi kara 

waxa se hadda caawiyay oo ay yeelatay framework
framework  waa meel ay kugu diyaarsan yihiin waxkasta oo aad uga baahanatahay css 
iyadoo website kaagana ka dhigaysa mid soo jiidasho heersare ah leh 
waxa kamid ah framework yadaas: 

1. Tailwindcss 
2. Bootstrap 
3. Bulmacss

hadaba dhamaantood isku shaqo ayay qabanayaan oo ah inay waqtiga kuu beekhaamiyaan 
hadii aad dhisayso system wayn oo u baahan in muddo koban lagu dhameeyo 
framework yadan waxa ay hadda marayaan verions kala duwan 
sanadkan 2022 ee dhamaadka ku dhaw 
waxa ay framework yadu kala marayaan 

1. Tailwindcss 3 // waxa iska leh nin lagu magacaabo adam wathan .
2 Bootstrap 5 // waxa iska leh twitter waana mid waayo kala duwan soo martay 
3. Bulmacss 0.9  // waa free waana open source macnheedu waxbaad ku darsan kartaa oo waa community 


css si aan ugu isticmaalo html maxaan u baahanay: 
3 hab ayaa jira oo css ay html ka ula macaamisho 

1. inline css // waxad ku dhex qoraysa element ka gudihiisa 
2. internal css // kan html ka meel kamid ah ayaad kaga soo dhex qori kartaa xaga sare ama xaga hoose ama dhexda 
3. external css // kan banaanka ayaa css looga saaraya iyadoo ugu dambayn si la isku qabadsiiyo shaqadii css iyo html la isku xidhi dono iyadoo la adeegsanayo link.


css waxyalaha kale ee ay awooda ku leedahay waxa kamid ah 
core attribute waana 4 nooc oo ah kuwa loogu isticmalka badan yahay css 100%

1. * waa xidigtaas waxa loo yaqaan :  universal selector
2. . dhibicdan oo u taagan  : class selector
3. # hashtag gan oo u taagan  : id selector
4. elements yadada oo u taagan:  tagname selector


* : 
kan waxa la isticmala hal mar oo ah markaad donayso dhamaan waxa meesha ku jira 
inaad wada maamusho hal mar 

. class : 
isaga waaka ugu awooda badan uguna shaqada badan 
class waxay katimid classification oo ah kala soocid 
hadii laba elment oo isku mid ah ay soo galaan html kaagi ama website kaagi 
waxa ad style ka ku kala maamulaysa class 

. id: 
id waa sida ka muuqata aqoonsi 
kaas oo u dhaw kii hore ee class balse 
marka css la joogo xadidan 

tagname: 
wa element kasta oo aad siinkarto style ka aad u baahantahay 
balse kaasi masi waaro oo waxa dhici karta in tusaale ahaan 
html kaagi ku jiraan 10 (h1) ama 20 (p) 
hadaba hadii aadoo tagname isticmalaay aad is tidhaah 
(p) gaar u maamul in ay saamayn ku yeelanayso dhamman kuwii kale ee ahaa (p)


